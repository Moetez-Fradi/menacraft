    r"it\s+is\s+(?:important|crucial|essential|worth)\s+to\s+(?:note|mention|highlight)|"
    r"it\'s\s+(?:important|crucial|essential|worth)\s+(?:noting|mentioning|highlighting)|"
    r"in\s+(?:conclusion|summary|closing)[,\s]|"
    r"to\s+(?:sum\s+up|summarize|conclude)[,\s]|"
