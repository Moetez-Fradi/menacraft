# ─────────────────────────────────────────────────────────────────────────────
# Axis 1 – Text classification
# ─────────────────────────────────────────────────────────────────────────────

def classify_text(text: str) -> dict:
    """
    Classify text as real | ai_generated | altered.

    Strategy (layered):
    1. Explicit AI-generation markers → ai_generated immediately
    2. Heuristic scoring (AI prose patterns, clickbait, conspiracy)
    3. roberta-base-openai-detector model
    4. Combine model + heuristic scores
    """
    if not text or len(text.strip()) < 10:
        return {
            "category":   "real",
            "confidence": 0.5,
            "highlights": [],
            "reasoning":  "Text too short for reliable classification.",
            "is_news":    False,
        }

    is_news = bool(_NEWS_SIGNALS.search(text))

    # ── Layer 1: explicit AI disclosure ──────────────────────────────────────
    if _EXPLICIT_AI_SIGNALS.search(text):
        matches = _EXPLICIT_AI_SIGNALS.findall(text)[:3]
        return {
            "category":   "ai_generated",
            "confidence": 0.92,
            "highlights": matches,
            "reasoning":  f"Explicit AI-generation markers found: {matches}",
            "is_news":    is_news,
        }

    # ── Layer 2: heuristic scoring ────────────────────────────────────────────
    heuristic_score, heuristic_reasons = _heuristic_text_score(text)

    # ── Layer 3: model inference ──────────────────────────────────────────────
    model_category  = "real"
    model_confidence = 0.5
    model_reasoning  = "Model unavailable; using heuristics."

    try:
        from models import get_text_classifier
        with torch.inference_mode():
            results = get_text_classifier()(text[:512], truncation=True)

        scores = {r["label"]: r["score"] for r in results[0]}
        top_label = max(scores, key=scores.get)
        top_score = scores[top_label]
        model_category   = _map_label(top_label)
        model_confidence = round(top_score, 4)
        model_reasoning  = (
            f"Detector model: '{top_label}' ({top_score:.2%} confidence). "
            + (f"Heuristic signals: {heuristic_reasons[:2]}" if heuristic_reasons else "")
        )
    except Exception as exc:
        model_reasoning = f"Model error ({exc}); heuristics only."

    # ── Layer 4: blend scores ─────────────────────────────────────────────────
    # Model score: 0 = real, 1 = ai_generated
    model_ai_score = model_confidence if model_category == "ai_generated" else (1 - model_confidence)

    # Combined score (70% model, 30% heuristic)
    combined = 0.70 * model_ai_score + 0.30 * heuristic_score

    if combined > 0.50:
        final_category   = "ai_generated"
        final_confidence = round(combined, 4)
        reasoning = model_reasoning
        if heuristic_reasons:
            reasoning += f" AI prose patterns: {heuristic_reasons[:3]}"
    elif combined > 0.40:
        final_category   = "altered"
        final_confidence = round(combined, 4)
        reasoning = f"Moderate AI signals ({combined:.2%}). " + model_reasoning
    else:
        final_category   = "real"
        final_confidence = round(1 - combined, 4)
        reasoning = model_reasoning

    return {
        "category":   final_category,
        "confidence": final_confidence,
        "highlights": heuristic_reasons[:5],
        "reasoning":  reasoning,
        "is_news":    is_news,
    }


def _heuristic_text_score(text: str) -> tuple[float, list[str]]:
    """
    Returns (0..1 ai-probability, list-of-matched-signals).
    Higher = more likely AI-generated or misleading.
    """
    reasons: list[str] = []
    score = 0.0

    # AI prose vocabulary
    ai_matches = _AI_PROSE_PATTERNS.findall(text)
    if ai_matches:
        n  = min(len(ai_matches), 8)
        score += n * 0.07
        reasons.extend([m if isinstance(m, str) and m else "AI prose pattern" for m in ai_matches[:4]])

    # Clickbait
    cb = _CLICKBAIT.findall(text)
    if cb:
        score += min(len(cb), 4) * 0.08
        reasons.append(f"clickbait language: {cb[:2]}")

    # Conspiracy
    cn = _CONSPIRACY.findall(text)
    if cn:
        score += min(len(cn), 4) * 0.10
        reasons.append(f"misinformation language: {cn[:2]}")

    # Very uniform sentence length (AI tends to write evenly-sized sentences)
    sentences = re.split(r"[.!?]+", text)
    sentences = [s.strip() for s in sentences if len(s.strip()) > 10]
    if len(sentences) >= 4:
        lengths = [len(s.split()) for s in sentences]
        mean    = sum(lengths) / len(lengths)
        variance = sum((l - mean) ** 2 for l in lengths) / len(lengths)
        if variance < 10 and mean > 12:  # very uniform, moderately long
            score += 0.12
            reasons.append("uniform sentence length (AI indicator)")

    return min(score, 1.0), reasons


def _map_label(label: str) -> str:
    """Map model output labels → our taxonomy."""
    label_lower = label.lower()
    # openai-community/roberta-base-openai-detector
    if label_lower == "fake":
        return "ai_generated"
    if label_lower == "real":
        return "real"
    # Hello-SimpleAI/chatgpt-detector-roberta
    if "chatgpt" in label_lower or "ai" in label_lower:
        return "ai_generated"
    if "human" in label_lower:
        return "real"
    # Generic fallback
    if any(k in label_lower for k in ("fake", "generated", "machine", "gpt", "synthetic")):
        return "ai_generated"
    if any(k in label_lower for k in ("alter", "manip", "edit")):
        return "altered"
    return "real"


