# Axis 1 – Image classification
# ─────────────────────────────────────────────────────────────────────────────

def classify_image(image_b64: str) -> dict:
    """
    Detect AI-generated / manipulated images via frequency-domain heuristics.
    AI-generated images have smoother spectral distributions (less high-freq noise).
    """
    try:
        raw   = base64.b64decode(image_b64)
        score = _heuristic_image_score(raw)

        if score > 0.65:
            category  = "ai_generated"
            reasoning = "High frequency-domain smoothness suggests AI synthesis (GAN/diffusion)."
        elif score > 0.40:
            category  = "altered"
            reasoning = "Moderate spectral inconsistency suggests post-processing or manipulation."
        else:
            category  = "real"
            reasoning = "Frequency distribution consistent with an unedited photograph."

        return {
            "category":   category,
            "confidence": round(score, 4),
            "highlights": [],
            "reasoning":  reasoning,
            "is_news":    False,
        }
    except Exception as exc:
        return {
            "category":   "real",
            "confidence": 0.3,
            "highlights": [],
            "reasoning":  f"Image analysis failed: {exc}",
            "is_news":    False,
        }


def _heuristic_image_score(raw_bytes: bytes) -> float:
    """
    FFT-based AI-image detector proxy.
    AI images (GAN/diffusion) have a higher ratio of energy in low frequencies.
    """
    try:
        import numpy as np
        from PIL import Image

        img = Image.open(io.BytesIO(raw_bytes)).convert("L").resize((64, 64))
        arr = np.array(img, dtype=np.float32)
        fft_shift = np.fft.fftshift(np.fft.fft2(arr))
        magnitude = np.abs(fft_shift)

        h, w = magnitude.shape
        cy, cx = h // 2, w // 2
        r = min(h, w) // 4

        y_idx, x_idx = np.ogrid[:h, :w]
        low_mask = (y_idx - cy) ** 2 + (x_idx - cx) ** 2 <= r ** 2
        low_energy  = magnitude[low_mask].sum()
        total_energy = magnitude.sum() + 1e-9

        low_ratio = low_energy / total_energy
        return float(min(low_ratio * 1.5, 1.0))
    except Exception:
        return 0.3


# ─────────────────────────────────────────────────────────────────────────────
# Axis 2 – Context confidence
# ─────────────────────────────────────────────────────────────────────────────

def check_context(text: str, image_b64: Optional[str]) -> dict:
    """
    Detect semantic mismatch between caption and image, OR misleading text alone.
    """
    if not text:
        return {
            "is_misleading": False,
            "confidence":    0.0,
            "explanation":   "No text provided.",
        }

    if image_b64:
        return _clip_check(text, image_b64)

    return _text_context_check(text)


def _clip_check(caption: str, image_b64: str) -> dict:
    try:
        from PIL import Image
        from models import get_clip

        raw   = base64.b64decode(image_b64)
        image = Image.open(io.BytesIO(raw)).convert("RGB")
        model, processor = get_clip()

        inputs = processor(
            text=[caption[:77]], images=image,
            return_tensors="pt", padding=True, truncation=True,
        )
        with torch.inference_mode():
            outputs    = model(**inputs)
            similarity = outputs.logits_per_image.softmax(dim=1)[0][0].item()

        is_misleading = similarity < 0.25
        confidence    = round(1.0 - similarity, 4)
        explanation   = (
            f"Image–caption CLIP similarity: {similarity:.2f}. "
            + ("Low similarity — image may not match the text description." if is_misleading
               else "Image and caption appear semantically consistent.")
        )
        return {"is_misleading": is_misleading, "confidence": confidence, "explanation": explanation}
    except Exception as exc:
        # Fall through to text-only check if CLIP fails
        return _text_context_check(caption)


def _text_context_check(text: str) -> dict:
    """
    Layered heuristics: recycled media, clickbait, conspiracy language.
    Returns first match found (most specific → most general).
    """
    # 1. Old/recycled media
    reuse = re.findall(
        r"\b(old\s+(?:photo|video|image|footage|clip)|from\s+\d{4}|"
        r"originally\s+(?:from|posted|shared)|taken\s+in\s+\d{4}|"
        r"archive\s+(?:photo|footage)|file\s+photo|stock\s+(?:image|photo)|"
        r"not\s+related\s+to|unrelated\s+to|misidentified)\b",
        text, re.IGNORECASE,
    )
    if reuse:
        return {
            "is_misleading": True,
            "confidence":    0.80,
            "explanation":   f"Recycled/misattributed media signals: {list(set(reuse))[:3]}",
        }

    # 2. Conspiracy / misinformation vocabulary
    conspiracy = _CONSPIRACY.findall(text)
    if conspiracy:
        return {
            "is_misleading": True,
            "confidence":    0.72,
            "explanation":   f"Misinformation language patterns: {list(set(conspiracy))[:3]}",
        }

    # 3. Heavy clickbait
    cb = _CLICKBAIT.findall(text)
    if len(cb) >= 2:
        return {
            "is_misleading": True,
            "confidence":    0.60,
            "explanation":   f"Sensationalist / clickbait language: {list(set(cb))[:3]}",
        }

    # 4. AI prose (misleading in a different way — synthetic content masquerading as human)
    ai_matches = _AI_PROSE_PATTERNS.findall(text)
    if len(ai_matches) >= 3:
        labels = [m for m in ai_matches if isinstance(m, str) and m.strip()][:3]
        return {
            "is_misleading": True,
            "confidence":    0.55,
            "explanation":   f"AI-generated prose patterns detected — content may be synthetic: {labels}",
        }

    return {
        "is_misleading": False,
        "confidence":    0.08,
        "explanation":   "No misleading context signals detected.",
    }
